package calculadora;

public class Modelo {

	public static int suma (int a, int b) {
		return a+b;
	}
	public static int resta (int a, int b) {
		return a-b;
	}
	public static double multiplicar (int a, int b) {
		return a*b;
	}
	public static double dividir (int a, int b) {
		return a/b;
	}
}
